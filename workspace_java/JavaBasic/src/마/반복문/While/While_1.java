package 마.반복문.While;

/*
    while(반복 조건){
        반복적으로 실행되는 내용
    }

*/
public class While_1 {
    public static void main(String[] args) {

        // 반복 시작점 무조건 있어야함
        int i = 1;

        while(i <= 3){ // 반복 조건 무조건 있어야함
            System.out.println(1);
            i++; // 반복을 파기할 조건 있어야함
        }
    }
}
